package edu.duke.cabig.c3pr.domain;

public class StudySiteCompanionStudyAssociation {
	
	public String organization ;
	public String irbApprovalDate;
	public String startDate ;
	public String targetAccrualNumber ;
	public String hostedMode ;
	public int companionStudySiteId ;
	public int studySiteId ;
	public int companionStudyAssociationId ;
	
	public String getOrganization() {
		return organization;
	}
	public void setOrganization(String organization) {
		this.organization = organization;
	}
	public String getIrbApprovalDate() {
		return irbApprovalDate;
	}
	public void setIrbApprovalDate(String irbApprovalDate) {
		this.irbApprovalDate = irbApprovalDate;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getTargetAccrualNumber() {
		return targetAccrualNumber;
	}
	public void setTargetAccrualNumber(String targetAccrualNumber) {
		this.targetAccrualNumber = targetAccrualNumber;
	}
	public String getHostedMode() {
		return hostedMode;
	}
	public void setHostedMode(String hostedMode) {
		this.hostedMode = hostedMode;
	}
	public int getCompanionStudySiteId() {
		return companionStudySiteId;
	}
	public void setCompanionStudySiteId(int companionStudySiteId) {
		this.companionStudySiteId = companionStudySiteId;
	}
	public int getStudySiteId() {
		return studySiteId;
	}
	public void setStudySiteId(int studySiteId) {
		this.studySiteId = studySiteId;
	}
	public int getCompanionStudyAssociationId() {
		return companionStudyAssociationId;
	}
	public void setCompanionStudyAssociationId(int companionStudyAssociationId) {
		this.companionStudyAssociationId = companionStudyAssociationId;
	}
	
	
}
