package edu.duke.cabig.c3pr.constants;

import static gov.nih.nci.cabig.ctms.domain.CodedEnumHelper.getByClassAndCode;
import static gov.nih.nci.cabig.ctms.domain.CodedEnumHelper.register;
import static gov.nih.nci.cabig.ctms.domain.EnumHelper.sentenceCasedName;
import gov.nih.nci.cabig.ctms.domain.CodedEnum;

public enum NotificationFrequencyEnum implements CodedEnum<String> {
    IMMEDIATE("Immediate"), WEEKLY("Weekly"), MONTHLY("Monthly"), ANNUAL ("Annual");

    private String code;

    private NotificationFrequencyEnum(String code) {
        this.code = code;
        register(this);
    }

    public String getCode() {
        return code;
    }

    public String getDisplayName() {
        return sentenceCasedName(this);
    }

    public String getName() {
        return name();
    }

    public static NotificationFrequencyEnum getByCode(String code) {
        return getByClassAndCode(NotificationFrequencyEnum.class, code);
    }
}